names = ["John", "Bob", "Mosh", "Sam", "Mary"] 
# Prints list as entered
print(names)

#Starts at beginning of list and returns the first entry
print(names[0])

#Starts at the end of the list and picks the first 
print(names[-1])

# Replaces an entry in the list
names[0] = "Aaron"
names[1] = "Laura"
print(names)

print(names[0:2])