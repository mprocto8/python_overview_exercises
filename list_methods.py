numbers = [1, 2, 3, 4, 5,]
print(numbers)
# Append Adds entry to end of list
numbers.append(6)
print(numbers)
# Insert adds entry into list at the integer you state
numbers.insert(0,-1)
print(numbers)

# Remove removes an entry from the list
numbers.remove(3)
print(numbers)

# Checks if something exists in a list and returns boolean (True or False)
print(2 in numbers)

# Returns how many items are in a list
print(len(numbers))

# Clears list
numbers.clear()
print(numbers)

